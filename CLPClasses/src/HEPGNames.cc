string
HEPGNames::name (int num)
{
   HNMapIter iter = sm_map.find (num);
   if ( iter != sm_map.end() )
   {
      // we found it
      char line[100];
      sprintf (line, "%s (%d)", iter->second.c_str(), num);
      return line;
   }
   else 
   {
      // we didn't find it
      char line[100];
      sprintf (line, "N/A (%d)", num);
      return line;
   } 
}

